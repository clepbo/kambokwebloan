package model;

public class Investment {
    String adsAmount, date, adsID, adsPackage, adsStatus, email, investAmt, investId;

    public String getAdsAmount() {
        return adsAmount;
    }

    public void setAdsAmount(String adsAmount) {
        this.adsAmount = adsAmount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getAdsID() {
        return adsID;
    }

    public void setAdsID(String adsID) {
        this.adsID = adsID;
    }

    public String getAdsPackage() {
        return adsPackage;
    }

    public void setAdsPackage(String adsPackage) {
        this.adsPackage = adsPackage;
    }

    public String getAdsStatus() {
        return adsStatus;
    }

    public void setAdsStatus(String adsStatus) {
        this.adsStatus = adsStatus;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getInvestAmt() {
        return investAmt;
    }

    public void setInvestAmt(String investAmt) {
        this.investAmt = investAmt;
    }

    public String getInvestId() {
        return investId;
    }

    public void setInvestId(String investId) {
        this.investId = investId;
    }
    
}
